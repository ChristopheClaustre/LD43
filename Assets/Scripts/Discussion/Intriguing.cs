    /********  OUR MESSAGES     ************************/

    /********  PUBLIC           ************************/

    public void NextIntriguing()
    {
        if (m_round >= m_discussion.Length)
            Discussions.Inst.EndDiscussion();
        else
        {
            Discussions.Inst.ChangeText(m_discussion[m_round]);
            m_round++;
        }
    }

    public void BeginIntriguing()
    {
        m_round = 0;
    }

